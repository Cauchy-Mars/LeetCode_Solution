/*
题目描述
小Q得到一个神奇的数列: 1, 12, 123,...12345678910,1234567891011...。
并且小Q对于能否被3整除这个性质很感兴趣。
小Q现在希望你能帮他计算一下从数列的第l个到第r个(包含端点)有多少个数可以被3整除。

输入描述:
输入包括两个整数l和r(1 <= l <= r <= 1e9), 表示要求解的区间两端。

输出描述:
输出一个整数, 表示区间内能被3整除的数字个数。

思路梳理：
1、找到规律，发现余数总是以1，0，0的顺序不断循环（用数学归纳法可证明）；
2、利用该规律找到通项公式，直接输出即可。
*/

#include <iostream>

using namespace std;


int main() {
    int l, r;
    cin >> l >> r;
    int ans = r-l+1 - (r+2)/3 + (l+1)/3;
    

    cout << ans << endl;

    return 0;
}